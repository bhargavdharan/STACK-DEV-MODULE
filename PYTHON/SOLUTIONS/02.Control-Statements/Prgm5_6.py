# Problem - 5 : Program to find the first digit of a number

# User input
num1 = int(input("Enter number : "))

# Convert int to string
str1 = str(num1)

firstNum = str1[0]

print("First digit of a number = "+firstNum)

# Problem - 6 : Program to find the last digit of a number

# User input
num2 = int(input("Enter number : "))

# Convert int to string
str2 = str(num2)

lastNum = str2[len(str2)-1]

print("Last digit of a number = "+lastNum)


