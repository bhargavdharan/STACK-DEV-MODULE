# Arithematical operators

a = 10
b = 20

# Addition (+)

# print("{}+{}={}")
print("{} + {} = {}".format(a,b,a+b))

# Subtraction (-)
print("{} - {} = {}".format(a,b,a-b))

# Multiplication (*)
print("{} X {} = {}".format(a,b,a*b))

# Division (/)
print("{} / {} = {}".format(a,b,a/b))

# Floor Division (//)
print("{} - {} = {}".format(a,b,a//b))

